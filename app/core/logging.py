"""Structured logging setup using structlog.

This is process-level logging (stdout), separate from the AIActionLog /
OrderEvent / HumanApproval audit trail stored in the database. Application
code should use both: log for operational visibility, DB audit rows for
compliance and dashboard visibility.
"""
import logging
import sys

import structlog


def configure_logging(debug: bool = True) -> None:
    log_level = logging.DEBUG if debug else logging.INFO

    logging.basicConfig(
        format="%(message)s",
        stream=sys.stdout,
        level=log_level,
    )

    structlog.configure(
        processors=[
            structlog.contextvars.merge_contextvars,
            structlog.processors.add_log_level,
            structlog.processors.TimeStamper(fmt="iso"),
            structlog.processors.StackInfoRenderer(),
            structlog.processors.format_exc_info,
            structlog.processors.JSONRenderer(),
        ],
        wrapper_class=structlog.make_filtering_bound_logger(log_level),
        context_class=dict,
        logger_factory=structlog.PrintLoggerFactory(),
        cache_logger_on_first_use=True,
    )


def get_logger(name: str):
    return structlog.get_logger(name)
